function hashId(id: string): number {
  return id.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
}

/** Pick a stable unique cover for any problem id */
export function getCoverImage(problemId: string, index?: number): string {
  if (index !== undefined && index >= 0 && index < COVER_IMAGES.length) {
    return COVER_IMAGES[index];
  }
  const match = problemId.match(/(\d+)$/);
  if (match) {
    const n = parseInt(match[1], 10) - 1;
    if (n >= 0 && n < COVER_IMAGES.length) return COVER_IMAGES[n];
  }
  return COVER_IMAGES[hashId(problemId) % COVER_IMAGES.length];
}

export function avatarColors(seed: string): string[] {
  const palette = ["#1B3A6B", "#2D6A4F", "#7B2D8B", "#B45309", "#BE123C", "#0369A1"];
  const i = seed.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
  return [
    palette[i % palette.length],
    palette[(i + 2) % palette.length],
    palette[(i + 4) % palette.length],
  ];
}

